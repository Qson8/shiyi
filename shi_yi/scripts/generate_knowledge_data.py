def generate_all_knowledge():
    """生成所有知识库数据"""
    items = []
    id_counter = 1
    
    # 生成形制科普数据
    for dynasty_data in SHAPE_TEMPLATES:
        dynasty = dynasty_data["dynasty"]
        for shape_data in dynasty_data["shapes"]:
            shape_name = shape_data["name"]
            for variant in shape_data["variants"]:
                title = f"{dynasty}{shape_name} - {variant}"
                content = generate_shape_content(dynasty, shape_name, variant)
                content += f"\n\n【文档标识】\n本文档ID: shape-{id_counter:04d}，专门介绍{dynasty}{shape_name}的{variant}相关内容。"
                
                item = {
                    "id": f"kb_{id_counter:04d}",
                    "title": title,
                    "category": "形制科普",
                    "content": content,
                    "tags": [dynasty, shape_name, "形制"],
                    "isFavorite": False,
                    "createdAt": datetime.now().isoformat()
                }
                items.append(item)
                id_counter += 1
    
    # 生成历史背景数据
    for theme_data in HISTORY_THEMES:
        theme = theme_data["theme"]
        for variant in theme_data["variants"]:
            title = f"{theme} - {variant}"
            content = generate_history_content(theme, variant)
            content += f"\n\n【文档标识】\n本文档ID: history-{id_counter:04d}，专门介绍{theme}的{variant}相关内容。"
            
            item = {
                "id": f"kb_{id_counter:04d}",
                "title": title,
                "category": "历史背景",
                "content": content,
                "tags": [theme, "历史", "文化"],
                "isFavorite": False,
                "createdAt": datetime.now().isoformat()
            }
            items.append(item)
            id_counter += 1
    
    # 生成穿搭指南数据
    for theme_data in STYLING_THEMES:
        theme = theme_data["theme"]
        for variant in theme_data["variants"]:
            title = f"汉服{theme} - {variant}"
            content = generate_styling_content(theme, variant)
            content += f"\n\n【文档标识】\n本文档ID: styling-{id_counter:04d}，专门介绍汉服{theme}的{variant}相关内容。"
            
            item = {
                "id": f"kb_{id_counter:04d}",
                "title": title,
                "category": "穿搭指南",
                "content": content,
                "tags": [theme, "穿搭", "指南"],
                "isFavorite": False,
                "createdAt": datetime.now().isoformat()
            }
            items.append(item)
            id_counter += 1
    
    return items

def main():
    print("开始生成知识库数据...")
    
    # 生成数据
    items = generate_all_knowledge()
    
    # 检查唯一性
    ids = [item['id'] for item in items]
    titles = [item['title'] for item in items]
    contents = [item['content'] for item in items]
    
    from collections import Counter
    duplicate_ids = {k: v for k, v in Counter(ids).items() if v > 1}
    duplicate_titles = {k: v for k, v in Counter(titles).items() if v > 1}
    duplicate_contents = {k: v for k, v in Counter(contents).items() if v > 1}
    
    if duplicate_ids:
        print(f"警告：存在重复的ID！{duplicate_ids}")
    if duplicate_titles:
        print(f"警告：存在重复的标题！共{len(duplicate_titles)}条")
    if duplicate_contents:
        print(f"警告：存在重复的内容！共{len(duplicate_contents)}条")
    
    # 统计分类
    categories = Counter([item['category'] for item in items])
    
    # 创建JSON数据
    data = {
        "version": "1.0",
        "lastUpdated": datetime.now().isoformat(),
        "totalCount": len(items),
        "items": items
    }
    
    # 保存到文件
    output_file = os.path.join(os.path.dirname(__file__), '../assets/data/knowledge_base.json')
    os.makedirs(os.path.dirname(output_file), exist_ok=True)
    
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f"\n生成完成！共生成 {len(items)} 条数据")
    for cat, count in sorted(categories.items()):
        print(f"{cat}: {count} 条")
    print(f"唯一ID数: {len(set(ids))}")
    print(f"唯一标题数: {len(set(titles))}")
    print(f"唯一内容数: {len(set(contents))}")
    print(f"数据已保存到: {os.path.abspath(output_file)}")

if __name__ == "__main__":
    main()
